    public Double showForm(Double[] results, String text) {
        currency = null;
        jlblPriceBuy.setText(null);
        jLblPriceSale.setText(null);
        jLblPriceReal.setText(null);
        jTxtPriceByCurrency.setText(null);
        jLblPriceByCurrencyCaption.setText(text);

        jlblPriceBuy.setText(Utils.toString(results[0]));
        jLblPriceSale.setText(Utils.toString(results[1]));
        jLblPriceReal.setText(Utils.toString(results[2]));
        this.setVisible(true);
        return currency;

    }

