    private void jBtnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAcceptActionPerformed
        if (jTxtPriceByCurrency.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Xahiş olunur məlumatları tam daxil edin!");
            return;
        }
        try {
            currency = (Double.parseDouble(jTxtPriceByCurrency.getText()));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Məbləği götürə bilmədi");
            return;
        }
        this.setVisible(false);
    }//GEN-LAST:event_jBtnAcceptActionPerformed
